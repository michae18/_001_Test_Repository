#include "stdio.h"

void main() {
	int val1 = 10, val2 = 20, result = 0;
	printf("관계연산자 : \n");

	result = val1 > val2;
	printf("%d\n", result);//alt 키를 누르면 그 줄 위치 이동.
	result = val1 < val2;
	printf("%d\n", result);
	result = val1 == val2;
	printf("%d\n", result);
	result = val1 >= val2;
	printf("%d\n", result);
	result = val1 <= val2;
	printf("%d\n", result);
	
}

